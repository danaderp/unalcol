package unalcol.agents.examples.squares.mirror;

/**
 *
 * @author Camilo
 */
public class Last {
    
    private int i;
    private int j;
    private String place;
    
    public Last(int i, int j, String s){
        
        this.i = i;
        this.j = j;
        this.place = s;
        
    }

    /**
     * @return the i
     */
    public int getI() {
        return i;
    }

    /**
     * @return the j
     */
    public int getJ() {
        return j;
    }

    /**
     * @return the place
     */
    public String getPlace() {
        return place;
    }
}
